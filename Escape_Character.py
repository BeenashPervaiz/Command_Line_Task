# output :Line A \n Line B
print ("Line A \\n Line B")
# output :Line A \t Line B
print ("Line A \\t Line B")
# output :Line A \b Line B
print ("Line A \\b Line B")
# output: \" \'
#why we use 3 backslash reason is that if \' = '   or \" = "
#       \\ = \   or \\ = \ 
#    \\\' = \'      or \\\" = \"  
print("\\\" \\\'")